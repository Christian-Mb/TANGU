        <script>
            var swiper = new Swiper('.swiper-container', {
                slidesPerView: 1.3,
                speed: 700,
                setWrapperSize: true,
                effect: 'slide',
                spaceBetween: 20,
                centeredSlides: true,
                pagination: {
                    el: '.swiper-pagination',
                    clickable: false,
                    dynamicBullets: true,
                },
            });
            
            var firstSlide = swiper.slides[0];
            firstSlide.style.boxShadow = "0px 4px 15px rgba(0, 0, 0, 40)";
            
            swiper.on('slideChangeTransitionStart', function() {
                var nbrOfSlides = swiper.slides.length;
                var currentSlideId = swiper.activeIndex;
                        
                for (var i = 0; i < nbrOfSlides; i++) {
                    swiper.slides[currentSlideId].style.transition = "box-shadow .3s ease-in-out";
                    swiper.slides[i].style.transition = "box-shadow .3s ease-in-out";
                    if (i != currentSlideId) {
                        swiper.slides[i].style.boxShadow = "0px 0px 0px rgba(0, 0, 0, 0)";
                    }
                }
                swiper.slides[currentSlideId].style.boxShadow = "0px 4px 15px rgba(0, 0, 0, 40)";
            });
